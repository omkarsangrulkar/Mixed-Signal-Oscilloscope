#define SAMPLE_RATE 1000.0f
#define TWO_PI 6.28318530718f

// Test signal generator function - IMPROVED VERSION
float OSC_GenerateTestSignal(uint16_t index, uint8_t signal_type) {
    float t = (float)index / SAMPLE_RATE;  // Time in seconds
    float voltage = 1.65f; // Ground reference (adjust via GND button if needed)

    switch(signal_type) {
        case 0: // Pure Sine Wave - 10Hz
        {
            float freq = 10.0f;
            voltage = 1.65f + 0.8f * sinf(TWO_PI * freq * t);
        }
        break;

        case 1: // Square Wave - 10Hz, 50% duty cycle
        {
            float period_ms = 100.0f;  // 10Hz = 100ms period
            float phase = fmodf((float)index, period_ms);
            voltage = (phase < (period_ms / 2.0f)) ? 2.5f : 0.8f;
        }
        break;

        case 2: // Triangle Wave - 10Hz
        {
            float freq = 10.0f;
            float period_samples = SAMPLE_RATE / freq;
            float phase = fmodf((float)index, period_samples);

            if(phase < period_samples / 2.0f) {
                // Rising edge
                voltage = 1.25f + (phase / (period_samples / 2.0f)) * 0.8f;
            } else {
                // Falling edge
                voltage = 2.05f - ((phase - period_samples / 2.0f) / (period_samples / 2.0f)) * 0.8f;
            }
        }
        break;

        case 3: // Sawtooth Wave - 10Hz
        {
            float freq = 10.0f;
            float period_samples = SAMPLE_RATE / freq;
            float phase = fmodf((float)index, period_samples);
            voltage = 0.8f + (phase / period_samples) * 1.7f;
        }
        break;

        case 4: // Pulse (Digital) - 5Hz, 10% duty
        {
            float period_ms = 200.0f;  // 5Hz = 200ms period
            float phase = fmodf((float)index, period_ms);
            voltage = (phase < 20.0f) ? 3.0f : 0.5f;
        }
        break;

        case 5: // Complex Waveform (Fundamental + 2nd & 3rd Harmonic)
        {
            float freq = 10.0f;
            voltage = 1.65f +
                      0.5f * sinf(TWO_PI * freq * t) +           // Fundamental
                      0.2f * sinf(TWO_PI * freq * 2.0f * t) +    // 2nd harmonic
                      0.1f * sinf(TWO_PI * freq * 3.0f * t);     // 3rd harmonic
        }
        break;

        case 6: // Noisy Sine - 10Hz with ±0.1V noise
        {
            float freq = 10.0f;
            float noise = ((rand() % 200) - 100) / 1000.0f;  // ±0.1V
            voltage = 1.65f + 0.8f * sinf(TWO_PI * freq * t) + noise;
        }
        break;

        case 7: // Exponential Decay (RC Circuit Simulation) - 5Hz
        {
            float period_ms = 200.0f;  // 5Hz
            float phase = fmodf((float)index, period_ms);

            if(phase < 5.0f) {
                // Short charge-up pulse
                voltage = 3.0f;
            } else {
                // Exponential decay (tau = 30ms)
                float decay_time = phase - 5.0f;
                voltage = 3.0f * expf(-decay_time / 30.0f);
            }
        }
        break;

        case 8: // PWM Signal - 1kHz, 25% duty (NEW!)
        {
            float period_ms = 1.0f;  // 1kHz
            float phase = fmodf((float)index, period_ms);
            voltage = (phase < 0.25f) ? 3.0f : 0.5f;
        }
        break;

        case 9: // Slow Ramp - 1Hz (NEW!)
        {
            float period_samples = SAMPLE_RATE * 1.0f;  // 1 second
            float phase = fmodf((float)index, period_samples);
            voltage = 0.5f + (phase / period_samples) * 2.5f;
        }
        break;

        default:
            voltage = 1.65f;  // DC level at ground
            break;
    }

    // Clamp to valid ADC range (0V to 3.3V)
    if(voltage < 0.0f) voltage = 0.0f;
    if(voltage > 3.3f) voltage = 3.3f;

    return voltage;
}

